# Fundamental ---- > int,float,str,boolean,complex

# integer -- int

x = 10
y = 20
y = 30

#type() -- gives the type of data assiging to variable
print(type(x))

# float -- float

x = 10.00
y = 11.12

print(x,type(x))
print(y,type(y))

# boolean --- bool

x = True 
y = False

print(x,type(x))
print(y,type(y))

# complex --- complex
x = 10+12j # real+imaginary

print(x,type(x))

print(x.real) # extracting real part in complex data
print(x.imag) # extracting imaginary part in complex data



